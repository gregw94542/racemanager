<div class="jobdescriptions">
<h2>Scribe (2 volunteers)</h2>
	<ol>
	<li>Aggregates data from timing tape(s) and writes data places and times on the heat cards</li>
	<li>The primary tape will be the time of record unless otherwise specified</li>
	<li>The secondary tape will used in the event of a missed click or a discrepancy</li>
	<li>Completed heat cards will be sent by runner to clerking</li>
	<li>Transfer image of heat card to clerking via photo/dropbox/google drive?  = Greg </li>
	<li>Laptop at both clerking/scribe with skype channel open for communications? </li>
	</ol>
</div>

<div class="jobdescriptions">
<h2>Lap Counter (2 volunteers)</h2>
	<ol>
	<li>Backup Lap counter  - Hand counter, used to check main lap counter</li>
	<li>Backup Lap counter becomes the lap counter for any lapped skaters</li>
	<li>Lap counter must be visible to the officials in the timing box </li>
	<li>bell will be rung at the beginning of the last lap</li>
	</ol>
</div>

<div class="jobdescriptions">
<h2>Clerking (2 volunteers)</h2>
	<ol>
	<li>Take data from heat card and enter into computer for scoring - this is data entry only, results are ready to be entered into the database at this point</li>
	<li>Print heat card from computer, post to window</li>
	<li>Following Race: Data entry must be completed within 15 minutes of pads off ice.</li>
	<li>any scoring/timing issues  will be resolved by one of the following<i>
	<br>Head Ref
	<br>Meet Director
	<br>Assistant Meet Director </i></li>
	<li>Complete data entry (do not enter questionable data into database)</li>
	<li>print results (with pr's) have official spot check from heat cards</li>
	<li>print results (with pr's) for award ceremony</li>
	<li>print PR certificates</li>
	<li>All NCSA officials on board to sign certificates</li>
	<li>provide Results and PR Certificates to Meet Director/President for award ceremony</li>
	</ol>
</div>

<div class="jobdescriptions">
<h2>Block Chasers (2 volunteers)</h2>
	<ol>
	<li>must have skating ability</li>
	<li>Responsible for moving blocks to appropriate positions </li>
	<li>Rotate track (move to different track) when directed to by head referee</li>
	<li>Block color must match starting line positions (500m, 1500m)</li>
	<li>should check whole track for continuity prior to beginning of event</li>
	<li>water and squeegee track </li>
	<li>replace kicked blocks during an event - with spare block in hand</li>
	<li>first priority is to be safe - block chasers will not enter field of play unless absolutely necessary</li>
	<li>remove displaced block  - more important than replacing block</li>
	<li>replace displaced block - so when safe</li>
	<li>block chasers will not impede an event</li>
	<li>when going back to position following a block replacement, do not impede racers or officials</li>
	<li>watch for offtrack fouls, report to referee</li>
	</ol>
</div>

<div class="jobdescriptions">
	<h2>Awards Ceremony</h2>
	<ol>
	<li>Must start in a timely fashion</li>
	<li>Acknowlege all volunteers and skaters</li>
	<li>Personal Records take precedence over placement</li>
	</ol>
</div>

