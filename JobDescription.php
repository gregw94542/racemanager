<h1>NCSA Race Day Job Descriptions</h1>	
<div class="body_print" style="text-align: left; padding: 10px; margin: 10px;">

<div class="jobdescriptions">
<h2>Check In (2-3 volunteers)</h2>
	<ol>
	<li>Ensure that all fees are collected</li>
	<li>check that the helmet number is attached to the helmet</li>
	<li>Ensure that the helmet number on helmet matches computer - <i>1 dedicated volunteer to handle this</i></li>
	<li>following close of check in
	<i>
	<br>print heat cards, split and distribute to Head Timer and heat box
	<br>print 7 copies of schedule of events - copies: to
	<br>starter
	<br>timing
	<br>lap counting
	<br>scribe
	<br>heat box
	<br>head ref
	<br>post in public areas)</i></li>
	</ol>

</div>	

<div class="jobdescriptions">
<h2>Heat Box (2-3 volunteers)</h2>
	<ol>
	<li>Assure that skaters are present prior to the start of race</li>
	<li>Equipment check new
	<i>
	<br>helmets
	<br>gloves
	<br>knee pads
	<br>long sleeve shirt
	<br>no skin showing at ankle
	<br>neckguard</i></li>
	<li>helmet number matches heat card</li>
	<li>Transition jackets and skate guards from entry door to exit door</li>
	<li>Watch doors where skaters enter and exit ice</li>
	<li>Ensure that doors are not opened prematurely - <i>Refer to an on-ice official</i></li>
	</ol>
</div>

<div class="jobdescriptions">
<h2>Starter (1)</h2>
	<ol>
	<li>Ensure that timer box is ready (green light)</li>
	<li>1 second delay between <strong>ready command</strong> and <strong>start of race</strong></li>
	<li>Starter in control of race until the apex block</li>
	<li>Following start, the Starter may transition from starter to assistant referee</li>
	<li>Starter will transition starter pistol after first series (8 races = ~500s) of races for reload </li>
	<li>Ensure that the starting block is on the correct starting line</li>
	<li>Understand current starting rules</li>
	<li>In the case of a foul or race disrupting event, a second shot or whistle will be used</li>
	</ol>
</div>
	
<div class="jobdescriptions">
<h2>Head Referee (1)</h2>
	<ol>
	<li>In control of race from the signup until the beginning of the awards ceremony</li>
	<li>Ensured that the field of play is safe<i>
	<br>doors
	<br>mats
	<br>proper block placement
	<br>start line properly marked
	<br>check for irregularities in the ice.</i></li>
	<li>Insures that proper safety equipment is worn by racers</li>
	<li>checks lane positions and helmet numbers</li>
	<li>Watch for lapped skaters</li>
	<li>Watch for fallen skaters in dangerous locations (call race is skater unable to move from this position)</li>
	<li>if problems with helmet numbers or safety equipment, all skaters in the current event will be
	removed from the field of play to correct the infraction.
	The event will be moved to the last event in the schedule. 	</li>
	</ol>
</div>



<div class="jobdescriptions">
<h2>Timing Box (Head Timer, Backup Timer, assistant timers) (6-8 volunteers)</h2>
	<ol>
	<li>heat cards with events and lane positions provided to timing prior to start of race</li>
	<li>programs 2 multi-lane timers based on heat card helmet numbers and lane positions</li>
	<li>single trigger printing stopwatch will always be used as a main timer backup</li>
	<li>Main multi-lane timer will be used for event timing, the second multi-lane timer will be held in reserve in the case of a false start</li>
	<li>The Head Timer will control the multi-lane timing mechanism, the Backup Timer will control the printing stopwatch </li>
	<li>In the case of a false start, the Head Timer and the Assistant Timers will transition to the second multi-lane timer </li>
	<li>A red light on the timing box indicates that the Timing team is not ready</li>
	<li>A green light on the timing box indicates that the Timing team is ready</li>
	<li>Following the completion of an event, the Head Timer will take the timing tapes, and staple to the heat card. The resulting package will be handed to the Scribe.</li>
	</ol>
</div>
